//Cap 26 - Ejemplos operadores logicos
let a = true;
let b = false;
console.log(a);
console .log(b);

//Operador logico && (and o Y)
//Regresa verdadero si ambos operadores son verdaderos, en cualquier otro caso es falso
console.log(`${a} && ${b} -> ${a&&b}`);



//Operador logico || (or o O)
//Regresa falso si ambos operadores son falsos, en cualquier otro caso es verdadero
console.log(`${a} || ${b} -> ${a||b}`);



//Operador logico ! (not o No)
//invierte el valor original, true -> false, false -> true
console.log(`${a} -> ${!a}`);
/*
Operadores Logicos
--------------------

&& And (y) Regresa verdadero si ambos valores logicos son verdaderos
Ej: y && x
True && True = True
True && False = False
False && True = False
False && False = False

|| or (o) Regresa verdadero si cualquiera de los valores logicos son verdaderos
Ej: y || x
True || True = True
True || False = True
False || True = True
False || False = False

! not (No) Invierte el resultado de True -> False o de False -> True
Ej: !x

*/

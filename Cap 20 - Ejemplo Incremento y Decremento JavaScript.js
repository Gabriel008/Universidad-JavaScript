//Operadores Incremento /Decremento
let a, b, c;

a = 0;
//Pre-incremento (Incremento al instante)
++a;
console.log(a);

//Post-incremento  (Incremento a para cuando se vuelva a utilizar la variable)
a++
console.log(a);



//Pre-decremento
--a;
console.log(a);

//Post-decremento
a--;
console.log(a);




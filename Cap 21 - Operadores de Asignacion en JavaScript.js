/*
Operadores de Asignacion

= Asignacion                            Ej x = 5;
+= Asignacion compuesto de suma         Ej x += 3  Equivalente a: x = x + 3

Tambien existen operdores compuestos de:
+=, -=, *=, /=, %=, entre varios mas

*/

//Operadores de Asignacion

let miNumero = 10;
console.log(miNumero)

miNumero = 20
console.log(miNumero)


// Operador +=
miNumero += 5
console.log(miNumero)


// Operador -=
miNumero -= 3
console.log(miNumero)


// Operador *=
miNumero *= 2
console.log(miNumero)


// Operador /=
miNumero /= 4
console.log(miNumero)


// Operador %=
miNumero %= 2
console.log(miNumero)


// Operador **=
miNumero = 8
miNumero **= 2
console.log(miNumero)